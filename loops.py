# while loop
num = 5
num1 = 100
num2 = 1
num3 = 50
while num <= 30:
    print(num)
    num += 5

while num1 >= 0:
    print(num1)
    num1 -= 10

while num2 < 10:
    print(num2)
    if num2 == 10:
        break
    num2 += 1

while num3 > 10:
    print(num3)
    if num3 == 10:
        break
    num3 -= 5
