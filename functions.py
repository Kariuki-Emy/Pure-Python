def maxvalue(a,b,c,d,e,f,g):
    return max(a,b,c,d,e,f,g)
myanswer=maxvalue(5,34,1,89,345,0,10)
print(myanswer)

def minvalue(a,b,c,d,e,f,g,h,i,j):
    return min(a,b,c,d,e,f,g,h,i,j)
myanswer=minvalue(10,25,6,55,21,300,105,65,91,4)
print(myanswer)

